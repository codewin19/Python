# write a program to input length and width of rectangle
# and print area and perimeter of rectangle
length = float(input('Enter length of rectangle : '))
width = float(input('Enter width of rectangle : '))
# code to calclulate perimeter
perimeter = 2 * (length+width)
# code to calculate area
area = length * width
# print output
print("Perimeter of rectangle is ",perimeter,"units and Area is ",area,"sq. units")
